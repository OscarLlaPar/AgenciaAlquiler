    private void mVehiculoNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mVehiculoNuevoActionPerformed
        if(this.dialogo.mostrar()==DialogoVehiculo.ACEPTAR){
            try {
                this.tMatricula.setText(dialogo.getMatricula());
                this.tGrupo.setSelectedItem(dialogo.getGrupo());
                this.tTipo.setSelectedItem(dialogo.getTipo());
                if(dialogo.getTipo().equals("TURISMO")){
                    this.tPlazas.setValue(dialogo.getPlazas());
                }
                else{
                    this.tCapacidad.setValue(dialogo.getCapacidad());
                }
                controlador.crearVehiculo();
                this.actualizarTabla();
            } catch (MatriculaException ex) {
                mostrarMensaje("Error en la matricula");
            }
        }
    }//GEN-LAST:event_mVehiculoNuevoActionPerformed

