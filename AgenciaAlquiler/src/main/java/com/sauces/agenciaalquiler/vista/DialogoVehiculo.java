    public String getMatricula(){
        return this.dTMatricula.getText();
    }
    
    public String getGrupo(){
        return (String)this.dTGrupo.getSelectedItem();
    }
    
    public String getTipo(){
        return (String)this.dTTipo.getSelectedItem();
    }
    
    public int getPlazas(){
        return (int)this.dTPlazas.getValue();
    }
    
    public float getCapacidad(){
        return Float.parseFloat(this.dTCapacidad.getText());
    }
    
    public int mostrar(){
        limpiarCampos();
        opcion=CANCELAR;
        setVisible(true);
        
        return opcion;
    }
    
    public void limpiarCampos(){
        this.dTMatricula.setText("");
        this.dTPlazas.setValue(0);
        this.dTCapacidad.setText("");
        
    }
    
