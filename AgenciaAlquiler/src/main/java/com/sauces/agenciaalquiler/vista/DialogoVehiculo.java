    private void dTTipoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_dTTipoItemStateChanged
        if (getTipo().equals("TURISMO")){
            dLCapacidad.setVisible(false);
            dTCapacidad.setVisible(false);
            dLPlazas.setVisible(true);
            dTPlazas.setVisible(true);
        }
        else{
            dLCapacidad.setVisible(true);
            dTCapacidad.setVisible(true);
            dLPlazas.setVisible(false);
            dTPlazas.setVisible(false);
        }
    }//GEN-LAST:event_dTTipoItemStateChanged

